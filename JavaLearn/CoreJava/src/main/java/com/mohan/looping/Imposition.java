package com.mohan.looping;

public class Imposition {

	public static void main(String[] args) {
		//For Loop Initialixation;Condition;Increment/Decrement
		for(int times=0;times<25;times++) {
			System.out.println("I can Program I can Learn");
		}
		//While Loop
		int times=0;
		while(times<25) {
			System.out.println("I can Program I can Learn");
			times++;
		}
		//Do While Loop
		//int times=0;
		do {
			System.out.println("I can Program I can Learn");
		}while(times<25);
	}

}
